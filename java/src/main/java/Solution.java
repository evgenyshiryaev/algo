import java.util.*;
import java.io.*;

public class Solution {

    public static void main(String[] args) {
        Scanner in = new Scanner(new BufferedReader(new InputStreamReader(System.in)));

        // test case number
        int tcs = in.nextInt();

        for (int tc = 1; tc <= tcs; ++tc) {
            int n = in.nextInt();

            int[] a = new int[n];
            for (int i = 0; i < n; ++i) {
                a[i] = in.nextInt();
            }

            System.out.println("RESULT");
        }
    }

}
